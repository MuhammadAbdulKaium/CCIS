anychart.onDocumentReady(function () {
    // create pie chart with passed data
    var chart = anychart.pie3d([
        ['Northfarthing', 235],
        ['Westfarthing', 552],
        ['Eastfarthing', 491],
        ['Southfarthing', 619],
        ['Buckland', 388],
        ['Westmarch', 1105]
    ]);

    // set chart title text settings
    chart
        .title('BCC')
        // set chart radius
        .radius('100%');

    // set container id for the chart
    chart.container('admin-chart');
    // initiate chart drawing
    chart.draw();
});
